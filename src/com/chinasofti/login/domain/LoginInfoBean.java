package com.chinasofti.login.domain;

public class LoginInfoBean {

	private String code;
	private String msg;
	private String dlcount;
	private String skipPath;
	
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}
	public String getDlcount() {
		return dlcount;
	}
	public void setDlcount(String dlcount) {
		this.dlcount = dlcount;
	}
	public String getSkipPath() {
		return skipPath;
	}
	public void setSkipPath(String skipPath) {
		this.skipPath = skipPath;
	}
	
	
	
	
}
